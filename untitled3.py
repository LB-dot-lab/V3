# 📌 Benutzer-Input im UI
initial_balance = st.sidebar.number_input("Startkapital ($)", value=10000)
reinvest_ratio = st.sidebar.slider("Reinvestitionsrate (%)", min_value=0, max_value=100, value=50)
risk_per_trade = st.sidebar.slider("Max. Risiko pro Trade (%)", min_value=1, max_value=10, value=2)
take_profit = st.sidebar.slider("Take-Profit (%)", min_value=1, max_value=100, value=10)
